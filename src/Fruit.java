public class Fruit {
    public float weight;
    public int amount;

    public Fruit (float weight) {
        this.weight = weight;
    }

    public int getAmount() {
        return amount;
    }

    public float getWeight() {
        return weight;
    }
    public void setAmount(int amount) {
        this.amount = amount;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }
}


